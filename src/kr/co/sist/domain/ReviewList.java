package kr.co.sist.domain;

public class ReviewList {

	private String re_num,user_id,re_title,re_inputdate;
	private int re_cnt;
	public String getRe_num() {
		return re_num;
	}
	public void setRe_num(String re_num) {
		this.re_num = re_num;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getRe_title() {
		return re_title;
	}
	public void setRe_title(String re_title) {
		this.re_title = re_title;
	}
	public String getRe_inputdate() {
		return re_inputdate;
	}
	public void setRe_inputdate(String re_inputdate) {
		this.re_inputdate = re_inputdate;
	}
	public int getRe_cnt() {
		return re_cnt;
	}
	public void setRe_cnt(int re_cnt) {
		this.re_cnt = re_cnt;
	}
	
	
}
